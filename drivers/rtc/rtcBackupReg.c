BOOL RtcBackupRegSet( UINT8 bBkpRegIndex, UINT32 dwValue )
{
    BOOL fSuccess = FALSE;

    if( bBkpRegIndex < RTC_BACKUP_REGS_AMOUNT )
    {
        if( RtcIsRtcConfigured() )
        {
            RTC_WriteBackupRegister( bBkpRegIndex, dwValue );

            fSuccess = TRUE;
        }
    }

    return fSuccess;
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//!
//!
//////////////////////////////////////////////////////////////////////////////////////////////////
BOOL RtcBackupRegGet( UINT8 bBkpRegIndex, UINT32 *pdwValue )
{
    BOOL fSuccess = FALSE;

    if( bBkpRegIndex < RTC_BACKUP_REGS_AMOUNT )
    {
        if( pdwValue != NULL )
        {
            if( RtcIsRtcConfigured() )
            {
                (*pdwValue) = RTC_ReadBackupRegister( bBkpRegIndex );

                fSuccess = TRUE;
            }
        }
    }

    return fSuccess;
}

